def asm1(value: int) -> int:
    if value > 0x3FB:  # 1019 in decimal
        if value == 0x559:  # 1369 in decimal
            return value - 0xA  # Subtract 10
        else:
            return value + 0xA  # Add 10
    else:
        if value == 0x280:  # 640 in decimal
            return value + 0xA  # Add 10
        else:
